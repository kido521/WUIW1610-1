var url=require('url');
var qs=require('querystring');
var u='http://www.baidu.com/index/a.php?foo=bar&baz=qux&bqe=quux&corge=123';
var urlStr=url.parse(u);
var query=urlStr.query;
var qObj=qs.parse(query);
qObj.baz="hello";
var queryStr=qs.stringify(qObj);
url.query=queryStr;
urlStr.search="?"+queryStr;
var uObj=url.format(urlStr);
console.log(uObj);